"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234)

# Aspas simples
print("Douglas Matos")
print(1, 'Douglas "Matos"')

# Aspas duplas
print("Douglas Matos")
print(1, "Douglas 'Matos'")

# Escape
print('Douglas "Matos"')

# r
print(r"Douglas \"Matos\"")

print('Explícito', 'é', 'melhor " do que implícito')